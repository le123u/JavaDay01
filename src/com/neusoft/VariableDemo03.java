package com.neusoft;

// 1.定义一个类，类名叫：VariableDemo03
public class VariableDemo03 {
    //2.定义main方法，作为程序的主入口
    public static void main(String[] args){

        //3.测试byte类型
        //3.1 定义一个byte类型的变量，变量名叫b,初始值为10
        byte b = 10;
        //3.2 将变量b打印到控制台
        System.out.println(b);
        /**
         * byte b;
         * b = 10;
         * System.out.println(b);
         */

        //4.测试short类型
        // 4.1定义一个short类型的变量，变量名叫s,初始值为20
        short s = 20;
        // 4.2将变量s打印到控制台
        System.out.println(s);
        /**
         * short s;
         * s = 20;
         * System.out.println(s);
         */

        //5.测试int类型
        //5.1定义一个int类型的变量，变量名叫i,初始值为30
        int i = 30;
        //5.2将变量i打印到控制台
        System.out.println(i);
        /**
         * int i ;
         * i = 30;
         * System.out.println(i);
         */

        //6.测试long类型
        //6.1定义一个long类型的变量，变量名叫lon,初始值为100,数据后记得加字母L
        long lon = 100L;
        //6.2将变量lon打印到控制台
        System.out.println(lon);
        /**
         * long lon;
         * lon = 100L;
         * System.out.println(lon);
         */

        //7.测试float类型
        //7.1定义一个float类型的变量，变量名叫f,初始值为10.3,数据后记得加字母F
        float f = 10.3F;
        //7.2 将变量f打印到控制台
        System.out.println(f);
        /**
         * float f;
         * f = 10.3F;
         * System.out.println(f);
         */

        //8.测试double类型
        //8.1定义一个double类型的变量，变量名叫d,初始值为5.21
        double d = 5.21;
        //8.2 将变量d打印到控制台
        System.out.println(d);
        /**
         * double d;
         * d = 5.21;
         * System.out.println(d);
         */

        //9.测试char类型
        //9.1定义一个char类型的变量，变量名叫c,初始值为'A'
        char c = 'A';
        //9.2将变量c打印到控制台
        System.out.println(c);
        /**
         * char c;
         * c = 'A';
         * System.out.println(c);
         */

        //10.测试boolean类型
        //10.1定义一个boolean类型的变量，变量名叫bo,初始值为True
        boolean bo = true;
        //10.2将变量bo打印到控制台
        System.out.println(bo);
        /**
         * boolean bo;
         * bo = true;
         * System.out.println(bo);
         */
    }
}
