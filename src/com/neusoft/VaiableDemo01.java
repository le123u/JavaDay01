package com.neusoft;

public class VaiableDemo01 {
    public static  void main(String[] args){

        // byte类型
        byte b ;
        b = 20;
        System.out.println(b);
        // short类型
        short s ;
        s = 30;
        System.out.println(s);
        // int类型
        int a ;
        a =  100;
        System.out.println(a);
        // long类型
        long lon;
        lon  = 100L;
        System.out.println(lon);
        // float 类型
        float f ;
        f = 10.4F;
        System.out.println(f);
        // double 类型
        double d ;
        d = 5.20;
        System.out.println(d);

        // char 类型
        char c ;
        c = 'A';
        System.out.println(c);

        // boolean类型
        boolean bb ;
        bb = false;
        System.out.println(bb);

    }
}
